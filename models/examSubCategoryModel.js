const mongoose = require("mongoose");

const subCatExamSchema = new mongoose.Schema(
  {
    subCatName: {
      type: String,
      enum: ["PRE", "MAINS", "OTHER"],
      required: [true, "Please Provide Sub Category as PRE / MAINS /OTHER"],
    },
    catName: {
      type: mongoose.Schema.Types.String,
      ref: "ExamCategory",
      required: [true, "Please Provide Exam Category Name"],
    },
  },
  { timestamps: true }
);

module.exports = mongoose.model("SubExamCategory", subCatExamSchema);

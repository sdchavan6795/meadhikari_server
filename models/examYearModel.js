const mongoose = require("mongoose");

const examYearSchema = new mongoose.Schema(
  {
    QPYear: {
      type: String,
      required: [true, "Please Provide Question Paper Year"],
    },
    catName: {
      type: mongoose.Schema.Types.String,
      ref: "ExamCategory",
      required: [true, "Please Provide Exam Category Name"],
    },
    subCatName: {
      type: mongoose.Schema.Types.String,
      ref: "SubExamCategory",
      required: [true, "Please Provide SubCategory Name"],
    },
  },
  { timestamps: true }
);

module.exports = mongoose.model("ExamYear", examYearSchema);
